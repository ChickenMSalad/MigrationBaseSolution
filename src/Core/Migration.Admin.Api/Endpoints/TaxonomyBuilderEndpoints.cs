    private static AprimoConfigurationWorkbookCredentials BuildAprimoCredentials(CredentialSetRecord credential)
    {
        var subDomain = FirstValue(credential.Values, "subDomain", "SubDomain", "aprimoSubDomain", "AprimoSubDomain", "tenant", "Tenant", "tenantName", "TenantName");
        var baseUrl = FirstValue(credential.Values, "baseUrl", "BaseUrl", "url", "Url", "tenantUrl", "TenantUrl", "aprimoBaseUrl", "AprimoBaseUrl");
        var clientId = FirstValue(credential.Values, "clientId", "ClientId", "aprimoClientId", "AprimoClientId", "TargetCredential_ClientId");
        var clientSecret = FirstValue(credential.Values, "clientSecret", "ClientSecret", "aprimoClientSecret", "AprimoClientSecret", "secret", "Secret", "TargetCredential_ClientSecret");

        if (string.IsNullOrWhiteSpace(subDomain) && !string.IsNullOrWhiteSpace(baseUrl))
        {
            subDomain = TryExtractSubDomain(baseUrl) ?? string.Empty;
        }

        if (string.IsNullOrWhiteSpace(subDomain))
        {
            throw new InvalidOperationException("Aprimo workbook generation requires a subDomain, aprimoSubDomain, tenant, or Aprimo baseUrl that contains the subdomain.");
        }

        if (string.IsNullOrWhiteSpace(clientId) || string.IsNullOrWhiteSpace(clientSecret))
        {
            throw new InvalidOperationException("Aprimo workbook generation requires clientId and clientSecret credentials.");
        }

        return new AprimoConfigurationWorkbookCredentials(subDomain, clientId, clientSecret, string.IsNullOrWhiteSpace(baseUrl) ? null : baseUrl);
    }

